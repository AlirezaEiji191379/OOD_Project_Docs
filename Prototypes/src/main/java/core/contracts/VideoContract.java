package core.contracts;

public class VideoContract {
    private int contentId;
    private int length;
    private String subtitle;
    private String file;

    public int getContentId() {
        return contentId;
    }

    public int getLength() {
        return length;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String getFile() {
        return file;
    }
}
